/* 1. Converta uma string para maiúsculas e minúsculas usando .toUpperCase() e 
.toLowerCase().*/
const TEXTO="Primeiro Exercicio de Strings";
const MAIUSCULAS=TEXTO.toUpperCase();
const MINUSCULAS=TEXTO.toLowerCase();
console.log("\nAqui está em Maiúsculas "+MAIUSCULAS);
console.log("\nAqui está em Minúsculas "+MINUSCULAS+"\n");