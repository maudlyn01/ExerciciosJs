/*3. Arredonde um número para cima, para baixo e para o inteiro mais próximo 
usando Math.ceil(), Math.floor() e Math.round(). */
const NUMERO=10.6;
const arrendondaCima=Math.ceil(NUMERO);
const arrendondaBaixo=Math.floor(NUMERO);
const arrendondaProximo=Math.round(NUMERO);
console.log("Arrendondando para cima o "+NUMERO+" fica: "+arrendondaCima);
console.log("Arrendondando para baixo o "+NUMERO+" fica: "+arrendondaBaixo);
console.log("Arrendondando para o mais proximo o "+NUMERO+" fica: "+arrendondaProximo);

